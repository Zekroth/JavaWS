package com.examples;

public class Employee {
	String nome;
	String cognome;
	int anni;
	String sesso;
	int stipendio;
	String matricola;
	/**
	 * @param nome
	 * @param cognome
	 * @param anni
	 * @param sesso
	 * @param stipendio
	 * @param matricola
	 */
	public Employee(String nome, String cognome, int anni, String sesso, int stipendio, String matricola) {
		super();
		this.nome = nome;
		this.cognome = cognome;
		this.anni = anni;
		this.sesso = sesso;
		this.stipendio = stipendio;
		this.matricola = matricola;

	}
	
	
}
